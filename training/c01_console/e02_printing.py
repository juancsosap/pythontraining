# help(print)

# Prints the givent text with a new line at the end (default)
print("Juan Sosa")

# Prints the texts with a space as a separator (default)
print("Juan","Sosa")

# Prints the texts with a given separator (sep)
print("SOSA","Juan", sep=", ")

# Prints the texts with a given ending (end)
print("Name", end=": ")
print("Juan", "Sosa")
