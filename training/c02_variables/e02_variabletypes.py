# Integer (int)
var1 = 123
print(type(var1))
print(dir(int), end="\n\n")

# Float (float)
var2 = 123.123
print(type(var2))
print(dir(float), end="\n\n")

# Boolean (bool)
var3 = True
print(type(var3))
print(dir(bool), end="\n\n")

# Complex (complex)
var4 = 123 + 123j
print(type(var4))
print(dir(complex), end="\n\n")

# String (str)
var5 = "Hello"
print(type(var5))
print(dir(str), end="\n\n")

# The variable type could be changed in any time
variable = 1
print(type(variable))
variable = 1.0
print(type(variable))
variable = True
print(type(variable))
variable = '1'
print(type(variable))
variable = 1j
print(type(variable))
